package org.code.tutorials.flow;

public class ConditionsD
{

	public static void main(String[] args)
	{
		int i = 2;
		
		if(i < 2)
		{
			System.out.println(i + " is less than 2");
		}
		else if(i > 2)
		{
			System.out.println(i + " is greater than 2");
		}
		else
		{
			System.out.println(i + " is equal to 2");
		}
	}

}
